#Programa que pregunta el largo y el ancho del rectángulo al usuario para dar el área
largo = float(input("¿Cuál es el lado del rectángulo en metros? "))
ancho = float(input("¿Cuál es el ancho del rectángulo en metros? "))

#Cálculo del área: largo * ancho
area = largo * ancho

#Mostrar resultado
print("El área del rectángulo es de ", area, "metros cuadrados.")